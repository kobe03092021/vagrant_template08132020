# モードラインを有効にする
set modeline

# モードラインの検索行数を設定
set modelines=5

# 新しい行のインデントを現在行と同じにする
set autoindent

# クリップボードをWindowsと連携
set clipboard=unnamed

# インクリメンタルサーチを行う
set incsearch

# 行番号を表示する
set number

# 閉じ括弧が入力されたとき、対応する括弧を表示する
set showmatch

# 検索時に大文字を含んでいたら大/小を区別
set smartcase

# 新しい行を作ったときに高度な自動インデントを行う
set smartindent